//Math.ceil是向上舍入,向上取得最为接近的整数
console.log('Math.ceil:',Math.ceil(25.9));
console.log('Math.ceil:',Math.ceil(25.5));
console.log('Math.ceil:',Math.ceil(25.1));

//标准的四舍五入
console.log('Math.round:',Math.round(25.9));
console.log('Math.round:',Math.round(25.5));
console.log('Math.round:',Math.round(25.1));


//Math.floor是向下舍入,向下取得最近的整数;
console.log('Math.floor:',Math.floor(25.9));
console.log('Math.floor:',Math.floor(25.5));
console.log('Math.floor:',Math.floor(25.1));